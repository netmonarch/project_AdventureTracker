The DM types in their available NPCs, and the form will autocomplete if their NPC is in their local adventure.<br />
If the NPC isn't there, it is added nonetheless. The DM will have to input its initiative score manually.<br />
The DM will also have to input the players' Initiative manually if they are not apart of the adventure.<br />
If the player and their character are in the initiative, they can roll initiative. The DM is alerted of this initiative before it is automatically added.<br />
Only the first initiative rolled is taken. It can be manually altered only by the DM.<br />