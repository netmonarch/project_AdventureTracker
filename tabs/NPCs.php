This is the DM's NPC factory. They can quickly add NPCs here, even without stats.<br />
Once an NPC is added, it is also added to a global database if it is a new NPC.<br />
Regular maintenence will require making sure that there are no duplicate NPCs.<br />
If an NPC exists, it is autocompleted. The DM will then be able to click the NPC and add it to their adventure for future use.<br />
Uses include Initiative, Location placement, and quests.